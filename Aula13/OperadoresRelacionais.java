package Aula13;

public class OperadoresRelacionais {

    public static void main(String[] args) {

         int valor1 = 1;
         int valor2 = 2;

        System.out.println("Valor 1 == valor2 : " + (valor1 == valor2)); // IGUAL A
        System.out.println("Valor 1 != valor2 : " + (valor1 != valor2)); // DIFERENTE DE
        System.out.println("Valor 1 > valor2 : " + (valor1 > valor2)); // MAIOR QUE
        System.out.println("Valor 1 < valor2 : " + (valor1 < valor2)); // MENOR QUE
        System.out.println("Valor 1 >= valor2 : " + (valor1 >= valor2)); // MAIOR OU IGUAL
        System.out.println("Valor 1 <= valor2 : " + (valor1 <= valor2)); // MENOR OU IGUAL
    }
}
